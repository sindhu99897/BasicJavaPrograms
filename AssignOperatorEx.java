public class AssignOperatorEx{
      public static void main(String args[]){
           int num1=60;
           System.out.println(num1);
           num1+=5;//num1=num1+5   
           System.out.println(num1);
           num1-=10;
           System.out.println(num1);
           num1*=2;
           System.out.println(num1);
           num1/=10;
           System.out.println(num1);
           num1%=5;
           System.out.println(num1);
  
   }
}