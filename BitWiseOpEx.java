public class BitWiseOpEx{
      public static void main(String args[]){
           int a=100;
           int b=200;  
           System.out.println(a&b);  
           System.out.println(a|b);    
           System.out.println(a^b);
           System.out.println(~a); 
           System.out.println(~b);   
           System.out.println(a>>b); 
           System.out.println(a<<b);  
           System.out.println(a>>>b);       
   }
}