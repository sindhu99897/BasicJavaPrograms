public class RelationalOpEx{
      public static void main(String args[]){
           int num1=30;
           int num2=60;
           System.out.println(num1);  
           System.out.println(num1==num2);  
           System.out.println(num1>=num2);  
           System.out.println(num1<=num2);  
           System.out.println(num1!=num2);  
           System.out.println(num1%=num2);  

   }
}